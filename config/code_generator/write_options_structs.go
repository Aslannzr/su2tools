func format_file(name string) error {
	f, err := os.Open(name)
	if err != nil {
		return err
	}
	b, err := ioutil.ReadAll(f)
	if err != nil {
		return err
	}
	b, err = format.Source(b)
	if err != nil {
		return err
	}
	f.Close()
	f, err = os.Create(name)
	if err != nil {
		return err
	}
	f.Write(b)
	f.Close()
	return nil
}

